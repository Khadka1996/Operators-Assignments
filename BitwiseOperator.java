public class BitwiseOperator {
    public static void main (String[]args){
        System.out.println(true & false);
        System.out.println(true | false);
        System.out.println(true ^ false); 
        System.out.println(8 & 5);
        System.out.println(4 | 9);
        System.out.println(8 ^ 10);
    }
}
